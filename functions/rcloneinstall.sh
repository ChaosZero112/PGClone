rcloneinstall () {

# install what version of rclone
rversion=1.53.2

rcheck1=$(rclone --version 2>&1)
rcheck2=$(echo ${rcheck1:8:6})

if [[ "$rversion" != "$rcheck2" ]]; then

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
💪 Installing RClone Version ~ $rversion
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

EOF
sleep 1.5

ansible-playbook ansible-playbook ${PGBLITZ_DIR}/ansible/rclone.yml

else

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
💪 Detected RClone Version ~ $rversion
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

EOF
sleep 1.5

fi

}

